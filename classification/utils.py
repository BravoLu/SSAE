def evaluate(test_loader, model ,generator, logs, delta, use_saliency_map=False, mean=[0.485,0.456,0.406], std=[0.229,0.224,0.225]):

    model.eval()
    all_raw_imgs, all_adv_imgs = [], []
    if generator:
        generator.eval()
    correct, total = 0, 0
    with torch.no_grad():
        for batch_idx, (inputs, targets) in enumerate(test_loader):
            raw_imgs, targets = inputs.cuda(), targets.cuda()
            if generator:
                perturbations, saliency_map = generator(inputs)
                if use_saliency_map:
                    adv_imgs =  raw_imgs + batch_clamp(delta, perturbations) * saliency_map
                else:
                    adv_imgs = raw_imgs + batch_clamp(delta, perturbations)
            else:
                adv_imgs = raw_imgs

            all_raw_imgs.append(raw_imgs.cpu())
            all_adv_imgs.append(adv_imgs.cpu())

            _, predicts = model(adv_imgs)
            _, predicts = predicts.max(1)
            total += targets.size(0)
            correct += predicts.eq(targets).sum().item()

            if batch_idx == 1:
                if generator:
                    save_image(saliency_map[:8], '%s/mask.jpg'%logs)
                    tensor2img(perturbations, mean=mean, std=std)
                    save_image(perturbations[:8], '%s/pertubations.jpg'%logs)

                tensor2img(adv_imgs, mean=mean, std=std)
                tensor2img(raw_imgs, mean=mean, std=std)
                save_image(adv_imgs[:8], '%s/adv.png'%logs)
                save_image(raw_imgs[:8], '%s/raw.png'%logs)

    all_raw_imgs = torch.cat(all_raw_imgs, dim=0)
    all_adv_imgs = torch.cat(all_adv_imgs, dim=0)

    acc = 100.*correct/total
    return acc, all_raw_imgs, all_adv_imgs


class AverageMeter(object):
    def __init__(self):
        self.val = 0
        self.avg = 0
        self.sum = 0
        self.count = 0

    def reset(self):
        self.val = 0
        self.avg = 0
        self.sum = 0
        self.count = 0

    def update(self, val, n=1):
        self.val = val
        self.sum += val * n
        self.count += n
        self.avg = self.sum / self.count
